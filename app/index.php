	<h3>正直は報われる</h3>
	「わたしたち​は​正直​な​良心​を​抱い​て​いる​と​信じ​て​い​ます。すべて​の​こと​に​おい​て​正直​に​行動​し​たい​と​願っ​て​いる​から​です」。―ヘブライ 13:18。<br>
	<br>
	聖書​中​で「正直」と​訳さ​れる​こと​の​ある​原語​の​ギリシャ​語​に​は，「本質​的​に​善」と​いう​意味​が​あり​ます。また，道徳​的​な​観点​から​美しい​こと，と​いう​意味​も​あり​ます。<br>
	<br>
	クリスチャン​は，使徒​パウロ​が​神​に​導か​れ​て​記し​た，「わたしたち​は……すべて​の​こと​に​おい​て​正直​に​行動​し​たい​と​願っ​て​いる」と​いう​言葉​を​真剣​に​受け止め​て​い​ます。では，すべて​の​こと​に​おい​て​正直​に​行動​する​に​は，どう​し​たら​よい​でしょ​う​か。<br>
	<br>
	<h4>自分​と​の​闘い</h4>
	
	大抵​の​人​は，毎朝，出かける​前​に​鏡​で​自分​を​見る​もの​です。一番​良い​自分​を​見せ​たい​から​です。と​は​いえ，おしゃれ​な​ヘアスタイル​を​し​たり​流行​の​服​を​着​たり​する​より​も​はるか​に​大切​な​こと​が​あり​ます。それ​は，自分​の​内面​が​どうか，と​いう​こと​です。実際，内面​が​外見​を​引き立てる​こと​も​あれ​ば​損なう​こと​も​ある​の​です。
	<br><br>
	聖書​が​率直​に​認め​て​いる​とおり，人間​に​は​悪い​こと​を​する​傾向​が​あり​ます。創世記 8​章​21​節​に​は，「人​の​心​の​傾向​は​その​年若い​時​から​悪い」と​述べ​られ​て​い​ます。ですから，わたしたち​は​生来​の​罪深い​傾向​と​闘わ​なけれ​ば​なり​ませ​ん。使徒​パウロ​も​罪​に​対する​自分​自身​の​闘い​に​つい​て，包み隠し​なく​こう​述べ​まし​た。「わたし​は，内​なる​人​に​し​た​が​え​ば​神​の​律法​を​ほんとう​に​喜ん​で​い​ます​が，自分​の​肢体​の​中​で​は​別​の​律法​が​わたし​の​思い​の​律法​と​戦い，わたし​を​とりこ​に​し​て​肢体​の​中​に​ある​罪​の​律法​へ​と​引い​て​行く​の​を​見​ます」。―ローマ 7:22，23。
	<br><br>
	わたしたち​に​も，悪​に​走ろ​う​と​する​衝動​や​不​正直​な​こと​を​し​よう​と​する​強い​傾向​が​あり​ます。しかし，そう​し​た​衝動​や​傾向​に​屈服​する​必要​は​あり​ませ​ん。正しい​道​を​選ぶ​こと​は​可能​です。悪い​考え​を​退ける​決意​を​貫く​なら，不​正直​な​世界​に​い​て​も​正直​で​いる​こと​が​でき​ます。
